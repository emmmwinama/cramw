                    <div id="eligibility" class="py-4 licencesection">
                        <h2 class="text-2xl font-bold mb-4 uppercase text-[#074923] text-justify">Eligible Applicants</h2>
                        <p class="text-justify">The Cannabis Regulation Act 2020 makes provision that CRA shall
                            issue a license or permit only to a “qualifying legal person” in respect
                            of the activity for which they are licensed to undertake under
                            this Act. Based on this provision, the following entities stand a
                            chance to be awarded a license upon meeting all requirements: (a)
                            Farmer organisations, (b) Corporate companies and (c) Corporate
                            partnerships. The Cannabis law is therefore promoting farmer
                            participation in the Cannabis value chain through registered
                            groups and not individual farming. The farmer organisations,
                            corporate companies and partnerships intending to participate
                            in Cannabis production and marketing are required to meet the
                            following eligibility requirements:  </p>
                        <span class="block mb-5"></span>
                        <div>
                            <span class="font-bold text-[#074923] text-lg">a) Farmers Organizations</span>
                            <div class="pl-6">Farmer organisations must, <br>
                                <div class="text-justify">
                                    <ol class="list-decimal pl-4 mt-2">
                                        <li>Possess a formal registration of their grouping</li>
                                        <li>Be composed of a minimum of 10 and a maximum of 100 farmers</li>
                                        <li>Possess a minimum land size of 5 hectares within a 5 kilometers radius from the most centrally located farm and a maximum of 10 fragmented pieces</li>
                                        <li>Submit a Police Clearance Certificate for Board
                                            of Directors or senior management of the farmer
                                            organization</li>
                                        <li>Submit off-taker agreement, if the organization will not
                                            process its own Cannabis</li>
                                        <li>Submit a business plan for the Cannabis enterprise</li>
                                        <li>Present GPS coordinates, size of land in hectares and the
                                            physical address of the plots</li>
                                    </ol>
                                </div>
                            </div>
                        </div>
                        <span class="block mb-5"></span>
                        <div>
                            <span class="font-bold text-[#074923] text-lg">b) Formal Partnerships</span>
                            <div class="pl-6">Formal partnerships must, <br>
                                <div class="text-justify">
                                    <ol class="list-decimal pl-4 mt-2">
                                        <li>Possess a legal agreement of their partnership
                                        </li>
                                        <li>Possess a minimum of un-fragmented land size of 5
                                            hectares
                                        </li>
                                        <li>Submit a Police Clearance Certificate of the partners</li>
                                        <li>Submit off-taker agreement, if the partnership will not
                                            process its own Cannabis
                                        </li>
                                        <li>Present GPS coordinates, size of land in hectares and the
                                            physical address of the plot
                                        </li>
                                        <li>Submit a comprehensive business plan for the Cannabis
                                            enterprise</li>
                                    </ol>
                                </div>
                            </div>
                        </div>
                        <span class="block mb-5"></span>
                        <div>
                            <span class="font-bold text-[#074923] text-lg">c) Corporate Companies</span>
                            <div class="pl-6">Corporate Companies must, <br>
                                <div class="text-justify">
                                    <ol class="list-decimal pl-4 mt-2">
                                        <li>Show proof of incorporation in Malawi
                                        </li>
                                        <li>Possess a minimum land size of 5 hectares that is not fragmented
                                        </li>
                                        <li>Submit a Police Clearance Certificate for Board of Directors</li>
                                        <li>Submit off-taker agreement, if the partnership will not process its own Cannabis
                                        </li>
                                        <li>Submit title deeds and GPS coordinates
                                        </li>
                                        <li>Submit a comprehensive business plan for the Cannabis
                                            enterprise</li>
                                    </ol>
                                </div>
                            </div>
                        </div>
