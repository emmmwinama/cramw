                    <div id="security" class="py-4 licencesection">
                        <h2 class="text-2xl font-bold mb-4 uppercase text-[#074923] text-justify">Security Requirements</h2>
                        <p>The security requirement for Cannabis is dependent on whether
                            it’s grown for medicinal or industrial purpose. Cannabis regulations
                            stipulate security plans that are applicable to industrial hemp and
                            medicinal cannabis. <span class="block mb-5"></span>
                            <span class="font-bold text-[#074923] block">Industrial hemp</span>
                            Interested applicants for industrial hemp license shall be required
                            to develop a security plan that prevents access to the industrial
                            hemp premises by unauthorised persons by establishing a natural
                            or a physical barrier around the farm. <span class="block mb-5"></span>
                            <span class="font-bold text-[#074923] block">Medicinal Cannabis</span>
                            Interested applicants for medicinal cannabis license shall be
                            required to develop a security plan that prevents access to the
                            premises by unauthorised persons by:
                        </p>
                        <span class="block mb-5"></span>
                        <div class="text-justify pl-6">
                            <ol class="list-decimal pl-4 mt-2">
                                <li>Establishing physical barriers which will secure perimeter
                                    access to all points of entry by setting up a fence around
                                    the ground that is at least two metres high and secured by
                                    a gate</li>
                                <li>Locating the field at least three kilometres from public
                                    institutions and places such as hospitals, schools, markets,
                                    unless if cultivation is done in a greenhouse surrounded by
                                    a barrier.</li>
                            </ol>
                        </div>
